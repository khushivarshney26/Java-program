/*8.wap in java to print 1 to n natural numbers by for loop */
import java.util.Scanner;

public class main8 {
  public static void main(String[] args) {
    Scanner input = new Scanner(System.in);

    System.out.print("Enter a number: ");
    int n = input.nextInt();

    for (int i = 1; i <= n; i++) {
      System.out.print(i + " ");
    }
  }
}
